package day12_forLoops;

public class C04_Faktoriyel {
    public static void main(String[] args) {
        int sayi =4;
        for (int i = 1; i <=sayi ; i++) {
            for (int j = 1; j <=sayi ; j++) {
                System.out.print(i*j+"  ");

            }
            System.out.println();
    }
}}
